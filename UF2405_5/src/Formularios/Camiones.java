    public void mostrarDatos() {
        String[] titulos = {"Matricula", "Modelo", "Tipo", "Potencia"};
        String[] registros = new String[4];
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        try {
            String SQL = "SELECT * FROM camiones;";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while (rs.next()) {
                registros[0] = rs.getString("Matricula");
                registros[1] = rs.getString("Modelo");
                registros[2] = rs.getString("Tipo");
                registros[3] = rs.getString("Potencia");
                modelo.addRow(registros);
            }
            tblCamiones.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de consulta" + e.getMessage());
        }
    }

    public void filtrarDatos(String valor) {
        String[] titulos = {"Matricula", "Modelo", "Tipo", "Potencia"};
        String[] registros = new String[4];
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        try {
            String SQL = "SELECT * FROM camiones where Matricula like '%" + valor + "%' or Modelo like'%" + valor + "%'";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while (rs.next()) {
                registros[0] = rs.getString("Matricula");
                registros[1] = rs.getString("Modelo");
                registros[2] = rs.getString("Tipo");
                registros[3] = rs.getString("Potencia");
                modelo.addRow(registros);
            }
            tblCamiones.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de consulta" + e.getMessage());
        }
    }

