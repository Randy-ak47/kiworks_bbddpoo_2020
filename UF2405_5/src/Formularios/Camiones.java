    public void insertarDatos() {
        try {
            String SQL = "insert into camiones (Matricula, Modelo, Tipo, Potencia) values (?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(SQL);
            pst.setString(1, txtMatricula.getText());
            pst.setString(2, txtModelo.getText());
            pst.setString(3, txtTipo.getText());
            pst.setString(4, txtPotencia.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Registro Nuevo");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Insertar datos");
        }
    }

    public void eliminarDatos() {
        try {
            int fila = tblCamiones.getSelectedRow();
            if (fila > 0) {
                String dao = (String) tblCamiones.getValueAt(fila, 0);
                String SQL = "delete from camiones where Matricula = " + dao;
                Statement st = con.createStatement();
                int n = st.executeUpdate(SQL);
                JOptionPane.showMessageDialog(null, "Registro eliminado");
            } else {
                JOptionPane.showMessageDialog(null, "Registro NO Seleccionado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en al eliminar " + e.getMessage());
        }
    }

    public void actualizarDatos() {
        try {
            String SQL = "update camiones set Modelo=?, Tipo=?, Potencia=? where Matricula = ?";
            int fila = tblCamiones.getSelectedRow();
            String nif = txtMatricula.getText();
            if (fila > 0) {
                String dao = (String) tblCamiones.getValueAt(fila, 0);
                PreparedStatement pst = con.prepareStatement(SQL);
                pst.setString(1, txtModelo.getText());
                pst.setString(2, txtTipo.getText());
                pst.setString(3, txtPotencia.getText());
                pst.setString(4, dao);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Registro actualizado");
            } else if (nif != null) {
                PreparedStatement pst = con.prepareStatement(SQL);
                pst.setString(4, txtMatricula.getText());
                pst.setString(1, txtModelo.getText());
                pst.setString(2, txtTipo.getText());
                pst.setString(3, txtPotencia.getText());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Registro actualizado");
            } else {
                JOptionPane.showMessageDialog(null, "Seleccionar un registro.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en el update " + e.getMessage());
        }
    }

